class Wall {
	/**
	* Position de la première moitié du mur
	*/
	private Boolean active = false;

	/**
    * Construit un mur a partir d'une position (objet Location)
    * @return true
    *			si ce mur est actif
    * @return true
    *			si ce mur n'est pas actif
 	*
    */
	public boolean isActive() {
		return this.active;
	}
}
